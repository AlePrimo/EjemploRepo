package ejemplorepo;


public class Alfajor {
    
    private String sabor;
    private int peso;

    public Alfajor(String sabor, int peso) {
        this.sabor = sabor;
        this.peso = peso;
    }

    public String getSabor() {
        return sabor;
    }

    public void setSabor(String sabor) {
        this.sabor = sabor;
    }

    public int getPeso() {
        return peso;
    }

    public void setPeso(int peso) {
        this.peso = peso;
    }
    
    
    
    
    
}
